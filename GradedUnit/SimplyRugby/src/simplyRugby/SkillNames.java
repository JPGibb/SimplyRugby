package simplyRugby;

//An enum is a data type that limits variables of its type to the values that are specified in the enum
//In this case it is being used to define the different skill names
//if a value that is not defined in the enum is assigned to a variable of its type and IllegalArgumentException will be thrown
public enum SkillNames {
	Standard,
	Spin,
	Pop,
	Front,
	Rear,
	Side,
	Scrabble,
	Drop,
	Punt,
	Grubber,
	Goal
}
